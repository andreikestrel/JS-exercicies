
console.log("A soma é " + soma(50, 2))
console.log("A divisão é " + divi(50, 2))
console.log("A subtração é " + subs(50, 2))
console.log("A multiplicação é " + mult(50, 2))

function soma(a, b) {
    return a + b
}
function divi(a, b) {
    return a / b
}
function subs(a, b) {
    return a - b
}
function mult(a, b) {
    return a * b
}
